-- 삭제트리거 (입고부분에서 삭제가 되면 재고부부에서 삭제된 수량을 빼준다)

CREATE OR REPLACE TRIGGER RECEIVING_DEL_TRIGGER
    AFTER DELETE ON RECEIVING
    FOR EACH ROW
BEGIN
    UPDATE PRODUCT SET STOCK = STOCK - (:OLD.RQTY) WHERE PCODE = :OLD.PCODE;
END;
/

